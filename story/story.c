void fileprint(char* f) {  //파일출력함수
    FILE* p = fopen(f, "r");
    if (!p)return;
    char line[500];
    while (fgets(line, sizeof(line), p))printf("%s", line);
    fclose(p);
}

int main() {
    int choice;

    while (1) {
        printMenu();
        printf("\n 숫자를 눌러 선택하세요: ");
        scanf("%d", &choice);

        switch (choice) {
        case 1:
            system("cls"); // 메뉴 화면 지우고 게임 시작
            startGame();
            break;
        case 2:
            loadGame();
            break;
        case 3:
            printf("게임을 종료합니다...\n");
            Sleep(700);
            return 0;
        default:
            printf("잘못된 입력입니다.\n");
            Sleep(700);
        }
    }
    return 0;
}

