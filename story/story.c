// --------------- 한 글자씩 출력하는 함수 ---------------
void slowPrintChar(const char* text, int delay) {
    while (*text) {
        // 키 눌렀는지 체크
        if (_kbhit()) {
            _getch(); // 눌린 키 입력 제거

            // 남은 텍스트 한 번에 출력
            printf("%s", text);
            fflush(stdout);
            return;
        }

        putchar(*text);
        fflush(stdout);
        Sleep(delay);
        text++;
    }
}

// --------------- 장면(여러 줄) 출력 후 화면 전환 ---------------
void scene(const char* text) {
    slowPrintChar(text, 28);

    printf("\n\n[Enter 키를 누르면 다음 장면으로 넘어갑니다]");

    while (1) {
        int key = _getch();
        if (key == '\r') break;  // Enter(ASCII 13)만 인정
    }

    system("cls");
}

